function [ phi1, phi2 ] = get_zeniths( r, z, L1, L2 )
%GET_ZENITHS returns the zenith angle of members L1 and L2.
phi1 = acos((L1^3 * z + L1 * z * (-L2^2+r^2+z^2)+(r^2+z^2) * ...
    sqrt(-((L1^2 * r^2 * (L1^4+(-L2^2+r^2+z^2)^2-2 * L1^2 * ...
    (L2^2+r^2+z^2)))/(r^2+z^2)^2)))/(2 * L1^2 * (r^2+z^2)));

phi2 = acos(-1*((L1^3 * z-L1 * z * (L2^2+r^2+z^2)+(r^2+z^2) * ...
    sqrt(-1*((L1^2 * r^2 * (L1^4+(-L2^2+r^2+z^2)^2-2 * L1^2 * ...
    (L2^2+r^2+z^2)))/(r^2+z^2)^2)))/(2 * L1 * L2 * (r^2+z^2))));
end

